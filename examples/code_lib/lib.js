/*   输入为空和都是空格、制表符和换行符判断为空  */
/*************************************************/
function isBlank(str){
  if(str === ""){
    return true;  //第一重判断，是否为空
  }

  var patt = /^\s+$/;

  if(patt.test(str)){
    return true;  //第二重判断，是否所有的都是空格、制表符和换行符 
  }

  return false;

}
/*************************************************/
/*****    判断输入的字符串是否为中文        ******/
/*-----------------------------------------------*/
/*        不能包含空格和各种标点符号             */
/*************************************************/
function isChinese(str){
  var patt = /^[\u4E00-\u9FA5]+$/;
  if(patt.test(str)){
    return true;
  }

  return false;
}
/*************************************************/
/*****    判断输入的字符串是否为邮箱        ******/
/*-----------------------------------------------*/
/*  由大小写字母，数字，中横线，下划线组成       */
/*  如果需要汉字，则在@前面加上\u4E00-\u9FA5     */
/*************************************************/
function isEmail(str){
  var patt = /^[a-zA-Z0-9-_]+@[a-zA-Z0-9-_]+(\.[a-zA-Z0-9-_]+)+$/;
  if(patt.test(str)){
    return true;
  }
  return false;
}
/*************************************************/
/*****  判断输入的字符串是否为合法的密码    ******/
/*-----------------------------------------------*/
/*        密码只允许由ascii组成[\x00-\x7f]       */
/*密码长度可以根据输入的后两位变量决定,默认6<x<17*/
/*************************************************/
function isValidPasswd(str,minLen,maxLen){
  var patt = /^[\x00-\x7f]+$/;
  if(!patt.test(str)){
    return false;
  }
  minLen = minLen || 6;
  maxLen = maxLen || 17;
  if((str.length > maxLen) || (str.length < minLen)){
    return false;
  }
  return true;
}
/*************************************************/
/*****    判断输入的字符串是否为整数        ******/
/*-----------------------------------------------*/
/*判断是否为正整数和负整数，输入可为字符串或数字 */
/*************************************************/
function isInt(str){
  str = str + ""; //转为字符串
  var patt = /^-?[0-9]+$/;
  if(patt.test(str)){
    return true;
  } 
  return false;
}
